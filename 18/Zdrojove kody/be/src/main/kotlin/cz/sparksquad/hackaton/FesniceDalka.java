package cz.sparksquad.hackaton;

public class FesniceDalka {

    private String name;

    private int distance;

    public FesniceDalka(String name, int distance) {
        this.name = name;
        this.distance = distance;
    }

    public int getDistance() {
        return distance;
    }

    public String getName() {
        return name;
    }

}
